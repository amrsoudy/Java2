
public abstract class Livre extends Things{

	private int noPages ;
	private String auter;
	

	public int getNoPages() {
		return noPages;
	}
	public void setNoPages(int noPages) {
		this.noPages = noPages;
	}
	public String getAuter() {
		return auter;
	}
	public void setAuter(String auter) {
		this.auter = auter;
	}

}
